#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			write (fd, "-2147483648", 11);
			return ;
		}
		write(fd, "-", 1);
		n *= -1;
	}
	if (n / 10 == 0)
	{
		ft_putchar_fd((n % 10) + '0', fd);
		return ;
	}
	i = n % 10;
	n /= 10;
	ft_putnbr_fd(n, fd);
	ft_putchar_fd(i + '0', fd);
}
